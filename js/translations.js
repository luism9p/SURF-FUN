// Lógica de cambio de idioma
// ============================================================
let currentLang = localStorage.getItem('userLang') || 'es';

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('userLang', lang);

  // Actualizar todos los elementos con data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key] !== undefined) {
      el.innerHTML = translations[lang][key];
    }
  });

  // Sincronizar estado visual de los botones de idioma
  document.querySelectorAll('.pill-lang-btn').forEach(btn => {
    const isActive = btn.getAttribute('data-lang') === lang;
    btn.classList.toggle('active', isActive);
  });

  // Actualizar estado visual de los botones
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.dataset.lang === lang) {
      // Activo: Agregar color turquesa, remover grises/transparentes
      btn.classList.add('text-[#00E5FF]', 'font-bold');
      btn.classList.remove('text-gray-400', 'text-white/50', 'text-white');
    } else {
      // Inactivo: Remover turquesa, agregar blanco translúcido
      btn.classList.remove('text-[#00E5FF]', 'font-bold');
      btn.classList.add('text-white/50');
    }
  });

  // Actualizar atributo lang del <html>
  document.documentElement.setAttribute('lang', lang);
}

// Conectar botones de la navbar al sistema de idiomas
document.addEventListener('DOMContentLoaded', () => {
  // Activar idioma guardado o predeterminado
  setLanguage(currentLang);

  // Vincular clicks en los botones de idioma
  document.querySelectorAll('.pill-lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang'));
    });
  });
});
